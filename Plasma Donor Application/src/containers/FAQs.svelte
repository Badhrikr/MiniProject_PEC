    <div class="collapse-title text-xl font-medium">
      What type of medical screening and testing is done?
    </div>
    <div class="collapse-content">
      <p>
        You must have a pre-donation physical which includes answering medical
        history questions, tests for viruses such as HIV and Hepatitis and
        evaluating your protein and hemoglobin levels.
      </p>
    </div>
  </div>

  <div
    tabindex="0"
    class="collapse collapse-plus border border-base-300 bg-base-100 rounded-box"
  >
    <div class="collapse-title text-xl font-medium">
      How do you get my plasma?
    </div>
    <div class="collapse-content">
      <p>
        Donating plasma is similar to giving blood. A needle is placed into a
        vein in your arm. Plasma is collected through a process call
        plasmapheresis and is conducted in cycles that may take up to an hour.
        Whole blood is drawn. The plasma is separated from the red blood cells
        and other cellular components. These are returned to your body with
        sterile saline solution to help the body replace the plasma removed from
        the whole blood.
      </p>
    </div>
  </div>

