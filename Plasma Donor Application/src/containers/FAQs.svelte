    <div class="collapse-title text-xl font-medium">
      Is donating plasma safe?
    </div>
    <div class="collapse-content">
      <p>
        Yes. Plasma donation in IQPP certified collection centers is performed
        in a highly controlled, sterile environment by professionally trained
        medical staff. All plasma collection equipment is sterilized and any
        equipment that comes into contact with you is used only once to
        eliminate the possibility of transmitting viral infections.
      </p>
    </div>
  </div>

  <div
    tabindex="0"
